    void loadAssignments(){
     try {
            // Load JDBC driver "com.mysql.jdbc.Driver"
            Class.forName("com.mysql.jdbc.Driver").newInstance();

            connection = DriverManager.getConnection(connectionURL, "root", "");

            mystmt = connection.createStatement();

            String sql="select * from add_assignments";

            rs=mystmt.executeQuery(sql);


            while(rs.next())
            {
                String assignment_id=String.valueOf(rs.getInt("assignment_id"));
                String assignment_name=rs.getString("assignment_name");
                String code1=String.valueOf(rs.getInt("code"));

                Object tdData[] = {assignment_id,assignment_name,code1};
                DefaultTableModel tblModel = (DefaultTableModel)jTable1.getModel();
                tblModel.addRow(tdData);
            }
        }
        // catch if found any exception during rum time.
        catch (Exception ex) {
             JOptionPane.showMessageDialog(null,"Found some error : "+ex);
        }

    }
