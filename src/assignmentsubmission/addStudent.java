    private void REGISTERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_REGISTERActionPerformed
        // TODO add your handling code here:
        try {
            // Load JDBC driver "com.mysql.jdbc.Driver"
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            connection = DriverManager.getConnection(connectionURL, "root", "");
            psmnt = connection.prepareStatement("insert into students(firstname,lastname,username,password) "+ "values(?,?,?,?)");
            psmnt.setString(1,first.getText());
            psmnt.setString(2,last.getText());
            psmnt.setString(3,user.getText());
            psmnt.setString(4,pass.getText());
            int s = psmnt.executeUpdate();     
            if(s>0) {
                
                JOptionPane.showMessageDialog(null,"Register successfully !");
            }
            else {
                JOptionPane.showMessageDialog(f,"unsucessfull registration. Contact Administrator","Alert",JOptionPane.WARNING_MESSAGE);
            }
             connection.close();
            psmnt.close();
        }
        // catch if found any exception during rum time.
        catch (Exception ex) {
           JOptionPane.showMessageDialog(f,"Found some error : ","Alert",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_REGISTERActionPerformed

